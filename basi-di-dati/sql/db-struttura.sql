FROM TOP_UTILIZZATORE_PER_STRUMENTO_ANNO TU
JOIN CONT_UTILIZZO_PER_STRUMENTO_ANNO CU ON TU.ID_STRUMENTO = CU.ID_STRUMENTO
ORDER BY ANNO, ID_STRUMENTO;



