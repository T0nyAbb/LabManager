--VIEW: MOSTRA IL RIEPILOGO MENSILE DI PRENOTAZIONI DI UNO STRUMENTO (IL NUMERO TOTALE DI PRENOTAZIONI E L'UTENTE CON PIU'
--PRENOTAZIONI PER UNO STRUMENTO IN UN DETERMINATO MESE)
