--VIEW: MOSTRA LE ORE DI UTILIZZO TOTALI PER UNO STRUMENTO IN UN DETERMINATO MESE
CREATE OR REPLACE VIEW CONT_UTILIZZO_PER_STRUMENTO_MESE(ID_STRUMENTO, MESE, ORE_UTILIZZO) AS
